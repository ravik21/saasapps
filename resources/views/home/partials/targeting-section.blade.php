<script>
document.addEventListener('DOMContentLoaded', function() {
    const container = document.querySelector('[data-targeting]');
    if (!container) return;
    let index = 0;
    const totalStates = 3; // 0, 1, 2
    let isIdle = false;
    let animationTimer = null;
    function updateState(newIndex) {
        index = newIndex;
        isIdle = false;
        // Update all elements based on current index
        updateCards();
        updateConnectors();
        updateDepartments();
        updateNotifications();
        // Reset isIdle after animation completes
        setTimeout(() => {
            isIdle = true;
        }, 1500);
    }
    function updateCards() {
        // Card 1 - Shows when index == 2 || index == 1
        const card1 = container.querySelector('.absolute.inset-0.flex.bg-gray-100:nth-of-type(1)');
        if (card1) {
            const show = index === 2 || index === 1;
            card1.style.display = show ? 'flex' : 'none';
            card1.style.zIndex = index === 1 ? '10' : index === 2 ? '20' : '30';
            card1.style.transform = index === 1 ? 'rotate(5deg)' : 'rotate(0deg)';
            card1.style.opacity = index === 1 ? '0.5' : index === 2 ? '1' : '0';
        }
        // Card 2 - Shows when index == 1 || index == 0
        const card2 = container.querySelector('.absolute.inset-0.flex.bg-gray-100:nth-of-type(2)');
        if (card2) {
            const show = index === 1 || index === 0;
            card2.style.display = show ? 'flex' : 'none';
            card2.style.zIndex = index === 0 ? '10' : index === 1 ? '20' : '30';
            card2.style.transform = index === 0 ? 'rotate(5deg)' : 'rotate(0deg)';
            card2.style.opacity = index === 0 ? '0.5' : index === 1 ? '1' : '0';
        }
        // Card 3 - Shows when index == 0 || index == 2
        const card3 = container.querySelector('.absolute.inset-0.flex.bg-gray-100:nth-of-type(3)');
        if (card3) {
            const show = index === 0 || index === 2;
            card3.style.display = show ? 'flex' : 'none';
            card3.style.zIndex = index === 2 ? '10' : index === 0 ? '20' : '30';
            card3.style.transform = index === 2 ? 'rotate(5deg)' : 'rotate(0deg)';
            card3.style.opacity = index === 2 ? '0.5' : index === 0 ? '1' : '0';
        }
        // Update button animations
        const buttons = container.querySelectorAll('button.relative.transition');
        buttons.forEach((btn, btnIndex) => {
            const shouldAnimate = (btnIndex === 0 && index === 2) ||
                                  (btnIndex === 1 && index === 1) ||
                                  (btnIndex === 2 && index === 0);
            if (shouldAnimate && !isIdle) {
                btn.classList.add('animate-fake-button-click');
            } else {
                btn.classList.remove('animate-fake-button-click');
            }
        });
    }
    function updateConnectors() {
        // Update all connector spans based on index
        const allSpans = container.querySelectorAll('span.absolute');
        allSpans.forEach(span => {
            // Hide all by default
            span.style.opacity = '0';
            span.style.display = 'none';
        });
        // Show relevant connectors based on index (simplified approach)
        // This would need to be more specific for each connector based on data attributes
    }
    function updateDepartments() {
        // Get all department items
        const deptItems = container.querySelectorAll('.grid.grid-cols-2 .relative.flex.justify-center');
        deptItems.forEach((item) => {
            const itemDiv = item.querySelector('div.transition');
            if (!itemDiv) return;
            const text = itemDiv.textContent.trim();
            // Determine if this department should be active
            let isActive = false;
            if (index === 0 && (text === 'Copenhagen' || text === 'Waiters')) isActive = true;
            if (index === 1 && (text === 'Stockholm' || text === 'Bartenders' || text === 'Chefs')) isActive = true;
            if (index === 2 && (text === 'Madrid' || text === 'Oslo' || text === 'Marketing')) isActive = true;
            if (isActive) {
                itemDiv.classList.add('text-gray-900', 'border-gray-900', 'shadow-department-selected');
                itemDiv.classList.remove('text-gray-900/50', 'border-gray-900/10', 'shadow-50');
                if (!isIdle) {
                    itemDiv.style.transitionDelay = '625ms';
                } else {
                    itemDiv.style.transitionDelay = '0ms';
                }
            } else {
                itemDiv.classList.remove('text-gray-900', 'border-gray-900', 'shadow-department-selected');
                itemDiv.classList.add('text-gray-900/50', 'border-gray-900/10', 'shadow-50');
                itemDiv.style.transitionDelay = '0ms';
            }
        });
    }
    function updateNotifications() {
        // Get all notification cards in the phone mockup
        const notifContainers = container.querySelectorAll('.relative.mx-3 > div.absolute');
        notifContainers.forEach((notif) => {
            const heading = notif.querySelector('h3');
            if (!heading) return;
            const text = heading.textContent.trim();
            // Determine visibility based on index and content
            let show = false;
            if (text === 'New Course' && (index === 2 || index === 0)) show = true;
            if (text === 'New To-do List' && (index === 1 || index === 2)) show = true;
            if (text === 'New Handbook' && (index === 0 || index === 1)) show = true;
            if (show) {
                notif.style.display = 'flex';
                notif.style.opacity = '1';
                // Update z-index and transform based on which is active
                if ((text === 'New Course' && index === 2) ||
                    (text === 'New To-do List' && index === 1) ||
                    (text === 'New Handbook' && index === 0)) {
                    notif.style.zIndex = '30';
                    notif.style.transform = 'scale(1) translateY(0)';
                } else {
                    notif.style.zIndex = '20';
                    notif.style.transform = 'scale(0.9) translateY(12px)';
                }
            } else {
                notif.style.display = 'none';
                notif.style.opacity = '0';
            }
        });
    }
    function nextState() {
        index = (index + 1) % totalStates;
        updateState(index);
    }
    // Initialize with index 0
    updateState(0);
    // Auto-cycle through states every 4 seconds
    animationTimer = setInterval(nextState, 4000);
    // Pause animation on hover
    container.addEventListener('mouseenter', () => {
        if (animationTimer) {
            clearInterval(animationTimer);
            animationTimer = null;
        }
    });
    container.addEventListener('mouseleave', () => {
        if (!animationTimer) {
            animationTimer = setInterval(nextState, 4000);
        }
    });
});
